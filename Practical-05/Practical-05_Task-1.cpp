#include <iostream>
using namespace std;
int main()
{
    char ch = 'F';
    bool b = true;
    short s = 1212;
    int i = 561269456;
    long l = 65218974232;
    float f = 456.56f;
    double d = 631.455;
    long double ld = 45354.669954;
    wchar_t wc = L'X';
    cout << "Character : " << ch << endl;
    cout << "Boolean : " << b << endl;
    cout << "Short Integer : " << s << endl;
    cout << "Integer : " << i << endl;
    cout << "Long : " << l << endl;
    cout << "Float : " << f << endl;
    cout << "Double : " << d << endl;
    cout << "Long Double : " << ld << endl;
    cout << "Wide Character : " << wc << endl;
    return 0;
}

